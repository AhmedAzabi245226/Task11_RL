def clearml_setup_and_sync_args(args) -> None:
    """
    Prevent recursion:
    - Worker (CLEARML_TASK_ID set): attach to current task and sync args from ClearML.
    - Local + --use_clearml: create task, connect args, enqueue, then EXIT immediately.
    """
    if not args.use_clearml and not os.environ.get("CLEARML_TASK_ID"):
        return  # no clearml at all

    from clearml import Task

    running_on_worker = bool(os.environ.get("CLEARML_TASK_ID"))

    if running_on_worker:
        # IMPORTANT: never enqueue on worker
        task = Task.current_task()
        if task is None:
            # Fallback: attach to the task id (prevents accidental Task.init recursion)
            task = Task.get_task(task_id=os.environ["CLEARML_TASK_ID"])

        cfg = task.connect(vars(args), name="cli_args")
        for k, v in cfg.items():
            if hasattr(args, k):
                setattr(args, k, _cast_like(getattr(args, k), v))
        return

    # Local machine: enqueue then exit
    task = Task.init(project_name=args.project_name, task_name=args.task_name)
    task.connect(vars(args), name="cli_args")
    task.set_base_docker(args.docker)

    print("[ClearML] Enqueuing task to queue:", args.queue)
    task.execute_remotely(queue_name=args.queue)
    raise SystemExit(0)


def upload_artifact(name: str, filepath: str):
    """Upload an artifact to ClearML with visible diagnostics."""
    try:
        from clearml import Task
        task = Task.current_task()
        if task is None:
            print(f"[upload_artifact] No current ClearML task. Skipping {name}.")
            return
        if not filepath:
            print(f"[upload_artifact] Empty filepath for {name}.")
            return
        if not os.path.exists(filepath):
            print(f"[upload_artifact] File not found for {name}: {filepath}")
            return

        size_mb = os.path.getsize(filepath) / (1024 * 1024)
        print(f"[upload_artifact] Uploading {name}: {filepath} ({size_mb:.2f} MB)")
        task.upload_artifact(name=name, artifact_object=filepath)
    except Exception as e:
        print(f"[upload_artifact] FAILED {name}: {e}")


def resolve_resume_local_path(args) -> str:
    """
    Resolve a local resume path usable on the worker.

    Priority:
    1) --resume_path: check path as-is, then PROJECT_ROOT/<resume_path>
    2) --resume_task_id + --resume_artifact: download via ClearML SDK (authenticated)
    """
    if args.resume_path and (args.resume_path.startswith("http://") or args.resume_path.startswith("https://")):
        raise RuntimeError(
            "resume_path is an HTTP(S) URL. ClearML files server often returns 401 to raw downloads.\n"
            "Use: --resume_task_id <TASK_ID> --resume_artifact ppo_final_model\n"
            "Or download locally and pass a local path that exists on the worker."
        )

    if args.resume_path:
        # Diagnostics help if worker CWD is not repo root
        print("[resume] CWD:", os.getcwd())
        print("[resume] PROJECT_ROOT:", str(PROJECT_ROOT))
        print("[resume] resume_path (raw):", args.resume_path)

        # 1) try as provided
        if os.path.exists(args.resume_path):
            return args.resume_path

        # 2) try relative to repo root
        candidate = os.path.join(str(PROJECT_ROOT), args.resume_path)
        print("[resume] trying PROJECT_ROOT-resolved:", candidate)
        if os.path.exists(candidate):
            return candidate

        # optional: list folder for debugging
        folder = os.path.join(str(PROJECT_ROOT), os.path.dirname(args.resume_path))
        if os.path.isdir(folder):
            try:
                print("[resume] listing:", folder, "->", os.listdir(folder))
            except Exception:
                pass

        raise FileNotFoundError(
            f"--resume_path not found.\nChecked: {args.resume_path}\nChecked: {candidate}\n"
        )

    if args.resume_task_id:
        from clearml import Task
        t = Task.get_task(task_id=args.resume_task_id)
        if args.resume_artifact not in t.artifacts:
            raise RuntimeError(
                f"Artifact '{args.resume_artifact}' not found in task {args.resume_task_id}. "
                f"Available: {list(t.artifacts.keys())}"
            )
        local = t.artifacts[args.resume_artifact].get_local_copy()
        print("[resume] Pulled from ClearML artifact:", local)
        return local

    return ""


